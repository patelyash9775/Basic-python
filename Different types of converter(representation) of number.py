#Decimal to octal,hexadecimal and binary
a=int(input("Enter a number : "))
print("Hexadecimal of ",a," is ",hex(a))
print("Octal of ",a," is ",oct(a))
print("Binary of ",a," is ",bin(a))

#Octal,hexadecimal and binary to decimal
print (int('010',8))
print (int('0xaa',16))
print (int('1010',2))